package lesson3;

public class WhileLoopLearning {
    public static void main(String[] args) {
        int t = 0;
        for (; t < 10; ) { // su dung khi biet so lan lap cu the
            System.out.println(t);
            t++;
        }

        while (t < 10) { // trong khi cai gi cung dung, thucj hien
            System.out.println(t);
            t++;
        }

    }
}
